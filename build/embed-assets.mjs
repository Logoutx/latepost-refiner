// Generate universal/embedded-assets.js — the assets a single-file binary must carry
// because, once compiled, there is no claude-code-skill/ or web/ directory on disk next to
// the executable. We inline them as plain JS string constants (runtime-agnostic: works the
// same under Node and Bun), and universal/assets.js prefers on-disk copies when present so a
// normal source checkout still picks up live edits. Run by build/build-binary.sh before the
// `bun build --compile` step. The output file is git-ignored (a transient build artifact).
import fs from 'node:fs'
import path from 'node:path'
import { fileURLToPath } from 'node:url'

const root = path.resolve(path.dirname(fileURLToPath(import.meta.url)), '..')
const read = (p) => fs.readFileSync(path.join(root, p), 'utf8')

const indexHtml = read('universal/web/index.html')
const refDir = 'claude-code-skill/references'
const references = Object.fromEntries(
  fs.readdirSync(path.join(root, refDir))
    .filter((f) => f.endsWith('.md'))
    .map((f) => [f, read(`${refDir}/${f}`)]),
)

const out = `// GENERATED by build/embed-assets.mjs — do not edit. Rebuilt on every binary build.
export default ${JSON.stringify({ indexHtml, references }, null, 0)}
`
const dest = path.join(root, 'universal/embedded-assets.js')
fs.writeFileSync(dest, out, 'utf8')
console.error(`embedded ${(out.length / 1024).toFixed(0)}KB → universal/embedded-assets.js (index.html + ${Object.keys(references).length} references)`)
