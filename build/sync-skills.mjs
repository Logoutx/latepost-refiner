#!/usr/bin/env node
// Single-source sync for the repo's vendored file copies.
//
// A few files ship in more than one place so each edition (the Claude Code skill and the Codex
// skill) is self-contained. Those copies are GENERATED from one source of truth and must never be
// hand-edited. This script rewrites every copy as: a DO-NOT-EDIT banner + the exact source bytes.
// `--check` rebuilds that expected content in memory and byte-compares each copy on disk — it is
// the ONE drift mechanism (wired into `pretest` and CI), replacing the old per-file `cmp` checks
// and the ad-hoc copy that build-cc.mjs used to do for the audit script.
//
//   npm run sync:skills                    # rewrite every copy from its source (idempotent)
//   node build/sync-skills.mjs --check     # verify; prints drifted paths, exits 1 on drift
//
// Why the banners are safe for the CC workflow bundle: the sources below live in the neutral tree
// (core/, scripts/, universal/) and NEVER get a banner — only their vendored copies do. build-cc.mjs
// embeds the SOURCES into claude-code-skill/workflow.js, so no banner ever reaches the sandbox guard.
//
// DELIBERATELY DIVERGENT — per-edition files that are NOT synced (each edition adapts them; do not
// add them to the manifest):
//   - claude-code-skill/SKILL.md, codex-skill/latepost-refiner/SKILL.md   (edition entry points)
//   - README.md
//   - references/deliverables.md, references/glossary-template.md          (per-edition wording)
//   - claude-code-skill/references/manual-steps.md, .../return-handling.md (Claude Code only)
//   - codex-skill/.../references/universal-runtime.md, .../native-runtime.md (Codex only)
//   - codex-skill/latepost-refiner/scripts/codex-native.mjs               (Codex-only runtime)
//   - codex-skill/latepost-refiner/agents/*.yaml                          (per-edition config)
//   - claude-code-skill/workflow.js                                        (GENERATED by build:cc)

import { readFileSync, writeFileSync, chmodSync, statSync } from 'node:fs'
import { fileURLToPath } from 'node:url'
import { dirname, join } from 'node:path'

const root = join(dirname(fileURLToPath(import.meta.url)), '..')

// Explicit, committed manifest: source relpath → [copy relpaths]. No globbing at sync time.
const MANIFEST = {
  'scripts/audit_refined.mjs': [
    'claude-code-skill/audit_refined.mjs',
    'codex-skill/latepost-refiner/scripts/audit_refined.mjs',
  ],
  'core/spec.js': ['codex-skill/latepost-refiner/core/spec.js'],
  'core/prompts.js': ['codex-skill/latepost-refiner/core/prompts.js'],
  'universal/artifacts.js': ['codex-skill/latepost-refiner/universal/artifacts.js'],
  'claude-code-skill/references/editorial-spec.md': [
    'codex-skill/latepost-refiner/references/editorial-spec.md',
  ],
  // Shell installers vendored per-edition (byte-identical duplicates). Bannered with '#' AFTER the shebang; the
  // executable bit is mirrored from the source on write so a fresh copy stays runnable / double-clickable.
  'scripts/setup-converters.sh': ['claude-code-skill/setup-converters.sh'],
  'scripts/install-converters.command': ['claude-code-skill/install-converters.command'],
}

// Expected bytes for a copy = a DO-NOT-EDIT banner + the exact source bytes. The banner is a comment
// appropriate to the file type (// for js/mjs, <!-- --> for markdown, # for shell .sh/.command). If the
// source starts with a shebang, the banner goes on the line AFTER it so the copy stays executable.
function expectedContent(sourceRel) {
  const src = readFileSync(join(root, sourceRel), 'utf8')
  const banner = sourceRel.endsWith('.md')
    ? `<!-- GENERATED FILE — DO NOT EDIT. Source: ${sourceRel}. Regenerate: npm run sync:skills -->`
    : (sourceRel.endsWith('.sh') || sourceRel.endsWith('.command'))
    ? `# GENERATED FILE — DO NOT EDIT. Source: ${sourceRel}. Regenerate: npm run sync:skills`
    : `// GENERATED FILE — DO NOT EDIT. Source: ${sourceRel}. Regenerate: npm run sync:skills`
  if (src.startsWith('#!')) {
    const nl = src.indexOf('\n')
    const shebang = nl >= 0 ? src.slice(0, nl) : src
    const rest = nl >= 0 ? src.slice(nl + 1) : ''
    return `${shebang}\n${banner}\n${rest}`
  }
  return `${banner}\n${src}`
}

function readOrNull(p) {
  try { return readFileSync(p, 'utf8') } catch { return null }
}

const check = process.argv.includes('--check')
const drifted = []
let wrote = 0
let total = 0
for (const [sourceRel, copies] of Object.entries(MANIFEST)) {
  const content = expectedContent(sourceRel)
  for (const copyRel of copies) {
    total += 1
    const copyPath = join(root, copyRel)
    const onDisk = readOrNull(copyPath)
    if (onDisk === content) continue
    if (check) {
      drifted.push(copyRel)
    } else {
      writeFileSync(copyPath, content)
      chmodSync(copyPath, statSync(join(root, sourceRel)).mode)   // mirror source mode so an executable source yields an executable copy
      wrote += 1
    }
  }
}

if (check) {
  if (drifted.length) {
    console.error(`sync:skills drift — ${drifted.length}/${total} vendored copy(ies) out of sync with their source:`)
    for (const d of drifted) console.error(`  ${d}`)
    console.error('Fix: run `npm run sync:skills` and commit the result.')
    process.exit(1)
  }
  console.log(`sync:skills --check: all ${total} vendored copies in sync`)
} else {
  console.log(`sync:skills: ${wrote} rewritten, ${total - wrote} already in sync (${total} copies total)`)
}
